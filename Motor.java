
public class Motor {
	public int Potencia ;
	public int Cilindradas ;
	
	Motor () {
		Potencia = 0;
		Cilindradas = 0;
	}

	public int getPotencia() {
		return Potencia;
	}

	public void setPotencia(int potencia) {
		Potencia = potencia;
	}

	public int getCilindradas() {
		return Cilindradas;
	}

	public void setCilindradas(int cilindradas) {
		Cilindradas = cilindradas;
	}

}
